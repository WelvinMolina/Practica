    public void obtenerDatos(){
       
      List<Revista>  Revistas=new DAORevista().ObtenerDatos();
      
      DefaultTableModel  modelo= new DefaultTableModel();
        
      String[] columnas={"jTextid_Revista","NumeroRev","TituloRev","AyoRev",
               "IssRev","PrecioRev", "HoraventaRev"};
      
      modelo.setColumnIdentifiers(columnas);
      for(Revista Revi :Revistas){
         
          String[]reglon={Integer.toString((int) Revi.getId_Revista(),Revi.getNumeroRev()), Revi.getTituloRev(),
               Revi.getAyoRev(),Revi.getIssRev(),
               Revi.getPrecioRev(), Revi.getHoraventaRev()};
            modelo.addRow(reglon);
     
      }
    jTableRegistro.setModel(modelo);
  }
    
    public void actualizarRevista(){
      int id=Integer.parseInt(this.jTextid_Revista.getText());
        String Nomb= jTextNumeroRev.getText();
        String titu=jTextTituloRev.getText();
        String ayo=jTextAyoRev.getText();
        String Iss=jTextIssRev.getText();
        String pre=jTextPrecioRev.getText();
        Date Hora=Time.valueOf(this.jTextHoraventaRev.getText());
        
        DAORevista dao=new DAORevista();
        int res=dao.Actualizar(id,Nomb,titu,ayo,Iss,pre,Hora);
        if (res==1){
             JOptionPane.showMessageDialog(rootPane,"¡Revista Actualizado!");
        }
        else{
            JOptionPane.showMessageDialog(rootPane,"¡Ocurrió un ERROR");
        }         
  }
    
