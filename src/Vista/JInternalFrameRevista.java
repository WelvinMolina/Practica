    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
    int fila=this.jTableRegistro.getSelectedRow();
        if(fila==-1){
            JOptionPane.showConfirmDialog(rootPane, "Seleccone un registro de la tabla");
        }
        else{
            try{
                int id_Revista=Integer.parseInt((String)this.jTableRegistro.getValueAt(fila,0).toString());
                
                String Nomb=(String)this.jTableRegistro.getValueAt(fila,1);
                String titu=(String)this.jTableRegistro.getValueAt(fila,2);
                String ayo=(String)this.jTableRegistro.getValueAt(fila,3);
                String Iss=(String)this.jTableRegistro.getValueAt(fila,4);
                String Hora=(String)this.jTableRegistro.getValueAt(fila,5);
                String pre=(String)this.jTableRegistro.getValueAt(fila,6);
               
               jTextid_Revista.setText(""+jTextid_Revista);
                jTextNumeroRev.setText(Nomb);
               jTextTituloRev.setText(titu);
               jTextAyoRev.setText(ayo);
               jTextIssRev.setText(Iss);
               jTextPrecioRev.setText(pre);
               jTextHoraventaRev.setText(Hora);
            }catch (NumberFormatException e){
                e.printStackTrace();
            }
        }
                                
    }//GEN-LAST:event_jBEditarActionPerformed

